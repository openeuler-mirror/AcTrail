def read_line_until(process: subprocess.Popen[str], stream, deadline: float) -> str:
    remaining = deadline - time.monotonic()
    if remaining <= 0:
        return ""
    ready, _, _ = select.select([stream], [], [], remaining)
    if not ready:
        return ""
    return stream.readline()


def wait_for_daemon(process: subprocess.Popen[str], timeout_sec: float) -> None:
    deadline = time.monotonic() + timeout_sec
    stderr_tail: list[str] = []
    while time.monotonic() < deadline:
        if process.stdout is not None:
            line = read_line_until(process, process.stdout, min(deadline, time.monotonic() + 0.2))
            if line:
                print(line, end="")
                if "daemon listening" in line:
                    return
        if process.poll() is not None:
            if process.stderr is not None:
                stderr_tail.append(process.stderr.read())
            raise RuntimeError(f"actraild exited early: {''.join(stderr_tail)}")
    raise RuntimeError("daemon did not become ready")


def run_checked(command: list[str]) -> str:
    result = subprocess.run(command, text=True, capture_output=True, check=False)
    if result.returncode != 0:
        raise RuntimeError(
            f"command failed rc={result.returncode}: {' '.join(command)}\nstdout={result.stdout}\nstderr={result.stderr}"
        )
    if result.stdout:
        print(result.stdout, end="")
    if result.stderr:
        print(result.stderr, end="", file=sys.stderr)
    return result.stdout


def decode_map(raw: str) -> dict[str, str]:
    def unescape(value: str) -> str:
        output: list[str] = []
        index = 0
        while index < len(value):
            char = value[index]
            if char == "\\" and index + 1 < len(value):
                escaped = value[index + 1]
                if escaped == "n":
                    output.append("\n")
                elif escaped == "e":
                    output.append("=")
                elif escaped == "\\":
                    output.append("\\")
                else:
                    output.append("\\")
                    output.append(escaped)
                index += 2
            else:
                output.append(char)
                index += 1
        return "".join(output)

    fields: dict[str, str] = {}
    for line in raw.splitlines():
        if "=" not in line:
            continue
        key, value = line.split("=", 1)
        fields[unescape(key)] = unescape(value)
    return fields


def wait_for_agent_pid(process: subprocess.Popen[str], timeout_sec: float) -> int:
    deadline = time.monotonic() + timeout_sec
    while time.monotonic() < deadline:
        line = read_line_until(process, process.stdout, deadline)
        if line.startswith("agent_pid="):
            print(line, end="")
            return int(line.split("=", 1)[1])
        if line:
            print(line, end="")
        if process.poll() is not None:
            raise RuntimeError("agent exited before reporting pid")
    raise RuntimeError("agent did not report pid")


def wait_for_agent_output(process: subprocess.Popen[str], timeout_sec: float) -> str:
    deadline = time.monotonic() + timeout_sec
    lines: list[str] = []
    while time.monotonic() < deadline:
        line = read_line_until(process, process.stdout, deadline)
        if line:
            print(line, end="")
            lines.append(line)
            if "denied_connect=" in line:
                break
        if process.poll() is not None:
            break
    output = "".join(lines)
    exit_code = process.wait(timeout=max(deadline - time.monotonic(), 0.1))
    if exit_code != 0:
        stderr = process.stderr.read() if process.stderr else ""
        raise RuntimeError(f"agent failed exit={exit_code}\nstdout={output}\nstderr={stderr}")
    return output


def wait_for_network_control_event(
    actrailctl: Path,
    config: Path,
    storage_path: Path,
    expected_endpoint: str,
    attempts: int,
    sleep_sec: float,
) -> dict[str, str]:
    for _ in range(attempts):
        run_checked([str(actrailctl), "--config", str(config), "list-traces"])
        with sqlite3.connect(storage_path) as connection:
            rows = connection.execute(
                "select payload_fields from events where trace_id = 1 and payload_variant = 'net'"
            ).fetchall()
        for (payload_fields,) in rows:
            fields = decode_map(payload_fields)
            if fields.get("remote") != expected_endpoint:
                continue
            metadata = decode_map(fields.get("metadata", ""))
            if (
                metadata.get("subject") == "network-action"
                and metadata.get("operation") == "connect"
                and metadata.get("decision_source") == "sync-plugin"
                and metadata.get("plugin_instance") == INSTANCE
                and metadata.get("rule_id") == "network-deny"
                and metadata.get("decision") == "deny"
            ):
                return metadata
        time.sleep(sleep_sec)
    raise RuntimeError("SQLite did not show expected network-action control event")


def parse_status_fields(output: str) -> dict[str, str]:
    fields: dict[str, str] = {}
    for line in output.splitlines():
        for item in line.split():
            if "=" in item:
                key, value = item.split("=", 1)
                fields[key] = value
    return fields


def stop_process(process: subprocess.Popen[str]) -> None:
    if process.poll() is not None:
        return
    process.send_signal(signal.SIGTERM)
    try:
        process.wait(timeout=5)
    except subprocess.TimeoutExpired:
        process.kill()
        process.wait()


def main() -> int:
    args = parse_args()
    require_root()
    bin_dir = ROOT / args.bin_dir
    actraild = require_binary(bin_dir, "actraild")
    actrailctl = require_binary(bin_dir, "actrailctl")
    agent_script = FIXTURE_DIR / "agent.py"

    allowed_server = TcpProbeServer("allowed")
    denied_server = TcpProbeServer("denied")
    allowed_server.start()
    denied_server.start()
    try:
        with tempfile.TemporaryDirectory(prefix="actrail-network-action-e2e-") as raw_tmp:
            tmp = Path(raw_tmp)
            rules = tmp / "network-rules.conf"
            config = tmp / "operator.conf"
            denied_endpoint = f"127.0.0.1:{denied_server.port}"
            write_text(
                rules,
                f"network-deny sync-plugin {INSTANCE} timeout-ms 5000 concurrency 1 fallback deny connect {denied_endpoint}\n",
            )
            write_text(config, operator_config(tmp, rules))
            write_text(tmp / "unused-enforcement-rules.conf", "")
            write_text(tmp / "unused-command-rules.conf", "")

            daemon = subprocess.Popen(
                [str(actraild), "--config", str(config), "run"],
                text=True,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
            )
            try:
                wait_for_daemon(daemon, args.daemon_ready_timeout_sec)
                run_checked(
                    [
                        str(actraild),
                        "--config",
                        str(config),
                        "plugin",
                        "load",
                        "--manifest",
                        str(FIXTURE_DIR / "deny-network.plugin.toml"),
                        "--instance",
                        INSTANCE,
                    ]
                )
                agent = subprocess.Popen(
                    [
                        str(actrailctl),
                        "--config",
                        str(config),
                        "launch",
                        "--name",
                        "network-action-control-plugin-e2e",
                        "--",
                        sys.executable,
                        str(agent_script),
                        "--allowed-port",
                        str(allowed_server.port),
                        "--denied-port",
                        str(denied_server.port),
                    ],
                    text=True,
                    stdin=subprocess.PIPE,
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                )
                try:
                    wait_for_agent_pid(agent, args.agent_timeout_sec)
                    if agent.stdin is None:
                        raise RuntimeError("agent stdin is not captured")
                    agent.stdin.write("go\n")
                    agent.stdin.flush()
                    output = wait_for_agent_output(agent, args.agent_timeout_sec)
                finally:
                    stop_process(agent)
                if "allowed_connect=ok" not in output or "denied_connect=permission_denied" not in output:
                    raise RuntimeError(f"agent output missed expected markers: {output}")
                if allowed_server.accepted != [b"allowed"]:
                    raise RuntimeError(f"allowed server did not receive exactly one request: {allowed_server.accepted!r}")
                if denied_server.accepted:
                    raise RuntimeError(f"denied server received data despite plugin denial: {denied_server.accepted!r}")
                wait_for_network_control_event(
                    actrailctl,
                    config,
                    tmp / "actrail.sqlite",
                    denied_endpoint,
                    args.drain_attempts,
                    args.drain_sleep_sec,
                )
                status = parse_status_fields(
                    run_checked(
                        [
                            str(actraild),
                            "--config",
                            str(config),
                            "plugin",
                            "status",
                            "--instance",
                            INSTANCE,
                        ]
                    )
                )
                if status.get("purpose") != "control-decider":
                    raise RuntimeError(f"plugin status missed purpose: {status}")
                if int(status.get("observed_records", "0")) != 1:
                    raise RuntimeError(f"plugin should observe exactly one network decision: {status}")
            finally:
                stop_process(daemon)
    finally:
        allowed_server.close()
        denied_server.close()
    print("network_action_control_e2e=ok")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
